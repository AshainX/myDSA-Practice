class Solution {
  public:
  
    void solve(unordered_map<int, int> &m, int &count, int curr, Node *root, int k ){
        if(!root) return;
        curr+=root->data;
        if(curr==k) count++;
        count+=m[curr-k]; 
        m[curr]++; //prefix sum
        solve(m,count, curr, root->left, k);
        solve(m,count, curr, root->right, k);
        
        //backtracking
        if(m[curr]==1)
            m.erase(curr);
        else
            m[curr]--;
    }
    int sumK(Node *root, int k) {
        // code here
        unordered_map<int, int> m;
        int count =0;
        solve(m, count, 0, root, k);
        return count;
    }
};

